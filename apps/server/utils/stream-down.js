const { parse } = require("node:url");
const http = require("node:https");
const fs = require("node:fs");
const pathUtil = require("node:path");
import ProgressBar from "progress";

module.exports = (url, path, size) => {
    const uri = parse(url);
    if (!path) {
        // biome-ignore lint/style/noParameterAssign: This is a workaround to allow the function to be called with a path parameter
        path = pathUtil.basename(uri.path);
    }
    const file = fs.createWriteStream(path);

    return new Promise((resolve, reject) => {
        http.get(uri.href).on("response", (res) => {
            const length = res.headers["content-length"]
                ? Number.parseInt(res.headers["content-length"], 10)
                : size;
            //   let downloaded = 0;
            //   let percent = 0;
            const bar = new ProgressBar(
                "  downloading [:bar] :rate/bps :percent :etas",
                {
                    complete: "=",
                    incomplete: " ",
                    width: 20,
                    total: length,
                },
            );

            res.on("data", (chunk) => {
                file.write(chunk);
                //   downloaded += chunk.length;
                //percent = ((100.0 * downloaded) / len).toFixed(2);
                bar.tick(chunk.length);
                //   process.stdout.write(
                //     `Downloading\t${percent}%\t${filesize(downloaded, {
                //       standard: 'iec',
                //     })}\t of ${filesize(len, {
                //       standard: 'iec',
                //     })}\t\t\t\t\t\t\r`,
                //   );
            })
                .on("end", () => {
                    file.end();
                    console.log(`\n${uri.path} downloaded to: ${path}`);
                    resolve(res);
                })
                .on("error", (error) => {
                    reject(error);
                });
        });
    });
};
